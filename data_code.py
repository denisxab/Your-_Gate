# Преобразовываем числа в кодировку CDC_21
def CDC_21(num_10x: int) -> str:
    # Compact Digit Coding 2021
    # Переводи число в вид '10000101' >>> ['10', '00', '01', '01']
    num_10x = wrap(str(num_10x), 2)
    res = ""
    for x in num_10x:
        res += CODING[x]
    return res


# Преобразовываем кодировки CDC_21 в число
def Number(cdc_21: str) -> str:
    # Compact Digit Coding 2021
    cdc_21 = list(cdc_21)
    res = ""
    for x in cdc_21:
        try:
            res += CODING_REVERS.get(x)
        except TypeError:
            res += ''
    return int(res)
